let c1=1
let c2=1

//WHILE SIMPLES / ESTRUTURA DE REPETIÇÃO COM TESTE NO INICIO
while(c1 <=5) {
    console.log(`Passo ${c1}`)
    c1++
}

console.log("")

//DO WHILE /ESTRUTURA DE RPETIÇÃO COM TESTE NO FINAL
do {
    console.log(`Passo ${c2}`)
    c2++
}while(c2<=5)

console.log("")

//FOR /ESTRUTURA DE REPETIÇÃO COM FINAL CERTO 
for(let c3= 1; c3<=5; c3++) {
    console.log(`Passo ${c3}`)
}